package com.akulgoyal.facade.subsystems;

public class PopcornPopper {

    public void on() {
        System.out.println("Popcorn popper is now live");
    }

    public void off() {
        System.out.println("Popcorn popper says goodbye");
    }

    public void pop() {
        System.out.println("Popping popcorn");
    }
}